package shiver.me.timbers.waiting.configuration;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.ComponentScan;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.EnableAspectJAutoProxy;
import shiver.me.timbers.waiting.WaiterAspect;

@Configuration
@ComponentScan("shiver.me.timbers.waiting")
@EnableAspectJAutoProxy
public class WaiterConfiguration {

    @Bean
    public WaiterAspect waiterAspect() {
        return new WaiterAspect();
    }
}
